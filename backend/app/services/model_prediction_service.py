from .prediction_service import PredictionService
from typing import Dict, Any
from tensorflow import keras
import numpy as np
import json
import os

# Match model_info.json
# input_shape = [60, 404]
TARGET_FRAMES = 60

# Match training feature_config
EXTRACT_HANDS = True
EXTRACT_POSE = True
EXTRACT_FACE = False

# Hand landmarks
# MediaPipe hand has 21 points.
# Each point has x, y, z.
# There are 2 hands: left and right.
HAND_DIM = 21 * 3 * 2  # 126

# Pose landmarks:
# Training uses first 25 pose landmarks.
# Each point has x, y, z.
POSE_DIM = 25 * 3      # 75

FACE_DIM = 0
D = HAND_DIM + POSE_DIM + FACE_DIM  # 201
F = D * 2 + 2                       # 404


BASE_DIR = os.path.abspath(
    os.path.join(os.path.dirname(__file__), "../../../")
)

MODEL_DIR = os.path.join(BASE_DIR, "models")



class ModelPredictionService(PredictionService):
    def __init__(self, model_path: str):
        self.model_path = os.path.join(MODEL_DIR, "lstm_model.keras")
        self.model_info_path = os.path.join(MODEL_DIR, "model_info.json")

        # Debug prints to check if backend can find model files
        print("MODEL PATH:", self.model_path)
        print("MODEL EXISTS:", os.path.exists(self.model_path))
        print("MODEL INFO PATH:", self.model_info_path)
        print("MODEL INFO EXISTS:", os.path.exists(self.model_info_path))

        self.model = self._load_model() #  Load Keras model and label names
        self.class_names = self._load_class_names()

    def _load_model(self):
        if not os.path.exists(self.model_path):
            print(f"Model not found: {self.model_path}")
            return None

        print(f"Loading Keras model from {self.model_path}")
        return keras.models.load_model(self.model_path)

    def _load_class_names(self):
        # model_info.json contains Thai label mapping.
        # Example: index 11 = "สวัสดี"
        if not os.path.exists(self.model_info_path):
            print("model_info.json not found. Using fallback labels.")
            return ["สวัสดี", "ขอบคุณ", "ใช่", "ไม่", "รัก"]

        with open(self.model_info_path, "r", encoding="utf-8") as f:
            info = json.load(f)

        return info["class_names"]

    def predict(self, landmarks_file: str) -> Dict[str, Any]:
        if self.model is None:
            return {
                "label": "model_not_loaded",
                "confidence": 0.0,
                "mode": "model_error"
            }

        try:
            # Read landmark JSON generated by MediaPipeService
            with open(landmarks_file, "r", encoding="utf-8") as f:
                landmarks_data = json.load(f)


            # Convert backend landmark JSON into model input shape:
            # (1, 60, 404)
            features = self._json_to_features(landmarks_data)

            proba = self.model.predict(features, verbose=0)[0]  # Run real model prediction
            
            # Get class with highest probability
            pred_idx = int(np.argmax(proba))
            confidence = float(proba[pred_idx])

            label = self.class_names[pred_idx] # Convert class index to Thai label using model_info.json

            return {
                "label": label,
                "confidence": round(confidence, 2),
                "mode": "model"
            }

        except Exception as e:
            print("Prediction error:", e)
            return {
                "label": "prediction_error",
                "confidence": 0.0,
                "mode": "model_error"
            }

    def _json_to_features(self, landmarks_data):
        """
        Convert MediaPipe JSON into model features.

        This part matches the training idea:
        hands + pose → raw keypoints
        raw keypoints → velocity
        add hand distance + dominant hand speed
        final shape → 60 frames × 404 features
        """
        keypoints = []

        for frame in landmarks_data:
            parts = []

            # Left hand: 21 landmarks × xyz = 63
            left_hand = frame.get("left_hand", [])
            if left_hand:
                lh = np.array([[p["x"], p["y"], p["z"]] for p in left_hand], dtype=np.float32).flatten()
            else:
                lh = np.zeros(63, dtype=np.float32)

            # Right hand: 21 landmarks × xyz = 63
            right_hand = frame.get("right_hand", [])
            if right_hand:
                rh = np.array([[p["x"], p["y"], p["z"]] for p in right_hand], dtype=np.float32).flatten()
            else:
                rh = np.zeros(63, dtype=np.float32)

            # Add both hands first.
            # This must match training feature order.
            parts += [lh, rh]

            # Pose: first 25 landmarks × xyz = 75
            pose = frame.get("pose", [])
            if pose:
                pose_arr = np.array([[p["x"], p["y"], p["z"]] for p in pose[:25]], dtype=np.float32).flatten()
            else:
                pose_arr = np.zeros(75, dtype=np.float32)

            parts.append(pose_arr)

            # One frame = 126 hands + 75 pose = 201 features
            keypoints.append(np.concatenate(parts))

        keypoints = np.array(keypoints, dtype=np.float32)
        
        
        # Velocity = movement difference between frames.
        # Same size as raw keypoints: 201
        velocity = np.zeros_like(keypoints)
        if len(keypoints) > 1:
            velocity[1:] = np.diff(keypoints, axis=0)

        # Left wrist is first hand landmark of left hand.
        # right wrist starts after 63 left hand features.
        left_wrist = keypoints[:, 0:3]
        right_wrist = keypoints[:, 63:66]
        
        # Distance between left and right wrist.
        # Shape: frames × 1
        hand_dist = np.linalg.norm(left_wrist - right_wrist, axis=1, keepdims=True)
        
        
        # Dominant hand speed.
        # Current version uses left wrist velocity.
        dom_speed = np.linalg.norm(velocity[:, 0:3], axis=1, keepdims=True)

        seq = np.concatenate([keypoints, velocity, hand_dist, dom_speed], axis=1)

        seq = self._normalize_sequence(seq)

        return seq[np.newaxis, ...].astype(np.float32)

    def _normalize_sequence(self, seq):
        
        """
        Normalize sequence to match model input:
        - exactly 60 frames
        - z-score normalization per feature
        """
        n_frames, n_feats = seq.shape

        # If already 60 frames, keep it
        if n_frames == TARGET_FRAMES:
            resampled = seq.copy()
            
        # If fewer than 60 frames, interpolate to 60
        elif n_frames < TARGET_FRAMES:
            x_old = np.linspace(0.0, 1.0, n_frames)
            x_new = np.linspace(0.0, 1.0, TARGET_FRAMES)
            resampled = np.stack(
                [np.interp(x_new, x_old, seq[:, i]) for i in range(n_feats)],
                axis=1
            )
        # If more than 60 frames, sample 60 evenly spaced frames
        else:
            indices = np.round(np.linspace(0, n_frames - 1, TARGET_FRAMES)).astype(int)
            resampled = seq[indices]

        # Z-score normalization:
        # each feature becomes roughly mean 0, std 1
        eps = 1e-8
        mean = resampled.mean(axis=0, keepdims=True)
        std = resampled.std(axis=0, keepdims=True)

        return ((resampled - mean) / (std + eps)).astype(np.float32)