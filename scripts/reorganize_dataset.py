def safe_move(src: Path, dst_dir: Path, log: list, dry_run: bool) -> str:
    """Move src into dst_dir, handling collisions by appending _dup_N suffix."""
    dst_dir.mkdir(parents=True, exist_ok=True)
    dst = dst_dir / src.name

    if dst.exists():
        stem = src.stem
        suffix = src.suffix
        n = 1
        while dst.exists():
            dst = dst_dir / f"{stem}_dup_{n}{suffix}"
            n += 1

    action = f"MOVE  {src}  →  {dst}"
    log.append(action)

    if not dry_run:
        shutil.move(str(src), str(dst))

    return action


def remove_if_empty(folder: Path, log: list, dry_run: bool):
    if folder.exists() and not any(folder.iterdir()):
        log.append(f"RMDIR {folder}  (empty after merge)")
        if not dry_run:
            folder.rmdir()


# ---------------------------------------------------------------------------
# Step 1: Merge duplicate folders
# ---------------------------------------------------------------------------

def merge_folders(data_dir: Path, merge_map: dict, log: list, dry_run: bool) -> dict:
    summary = {}

    for src_name, canonical_name in merge_map.items():
        src_dir = data_dir / src_name
        dst_dir = data_dir / canonical_name

        if not src_dir.exists():
            log.append(f"SKIP  {src_name}  (folder not found)")
            continue

        files = list(src_dir.glob("*"))
        moved = []

        for f in files:
            if f.is_file():
                action = safe_move(f, dst_dir, log, dry_run)
                moved.append(f.name)

        remove_if_empty(src_dir, log, dry_run)

        summary[src_name] = {
            "canonical": canonical_name,
            "files_moved": len(moved),
            "files": moved,
        }

    return summary


# ---------------------------------------------------------------------------
# Step 2: Classify unknown files
# ---------------------------------------------------------------------------

def classify_unknown(data_dir: Path, classification: dict, log: list, dry_run: bool) -> dict:
    unknown_dir = data_dir / "unknown"
    if not unknown_dir.exists():
        log.append("SKIP  unknown/  (folder not found)")
        return {}

    summary = {"classified": [], "unclassified": []}

    for f in sorted(unknown_dir.iterdir()):
        if not f.is_file():
            continue

        stem = f.stem  # filename without .mp4

        target_name = classification.get(stem)

        if target_name is None:
            log.append(f"UNCLASSIFIED  {f.name}")
            summary["unclassified"].append(f.name)
            continue

        target_dir = data_dir / target_name
        if not target_dir.exists() and not dry_run:
            target_dir.mkdir(parents=True, exist_ok=True)
            log.append(f"MKDIR {target_dir}  (new category)")

        action = safe_move(f, target_dir, log, dry_run)
        summary["classified"].append({"file": f.name, "target": target_name})

    remove_if_empty(unknown_dir, log, dry_run)
    return summary


# ---------------------------------------------------------------------------
# Step 3: Generate report
# ---------------------------------------------------------------------------

def generate_report(merge_summary: dict, unknown_summary: dict, log: list, dry_run: bool):
    LOG_DIR.mkdir(parents=True, exist_ok=True)
    ts = datetime.now().strftime("%Y%m%d_%H%M%S")
    mode = "DRY_RUN" if dry_run else "EXECUTED"
    log_path = LOG_DIR / f"reorganize_{mode}_{ts}.log"
    json_path = LOG_DIR / f"reorganize_{mode}_{ts}.json"

    # Plain log
    with open(log_path, "w", encoding="utf-8") as fh:
        fh.write(f"=== Dataset Reorganization Log [{mode}] ===\n")
        fh.write(f"Timestamp: {ts}\n\n")
        for line in log:
            fh.write(line + "\n")

    # JSON summary
    report = {
        "mode": mode,
        "timestamp": ts,
        "merge_summary": merge_summary,
        "unknown_classification": unknown_summary,
        "total_actions": len(log),
    }
    with open(json_path, "w", encoding="utf-8") as fh:
        json.dump(report, fh, indent=2, ensure_ascii=False)

    return log_path, json_path


